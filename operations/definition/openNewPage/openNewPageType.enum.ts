export enum OpenNewPageType {
  /**
   * Refers to a event where a new tab is opened in the browser
   *
   * @type {OpenNewPageType.PAGEOPEN}
   */
  PAGEOPEN = 'PAGEOPEN',
  /**
   * Refers to a event where the current page is redirected to a new URL
   *
   * @type {OpenNewPageType.PAGE}
   */
  PAGE = 'PAGE',
}
